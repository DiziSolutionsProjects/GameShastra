  document.addEventListener('DOMContentLoaded', function() {
    // Your image data
    const images = [
      'assets/images/footer/e1.png',
      'assets/images/footer/e2.png',
      'assets/images/footer/a3.png',
      'assets/images/footer/a4.png',
      'assets/images/footer/a5.png',
      'assets/images/footer/a6.png',
      'assets/images/footer/a7.png',
      'assets/images/footer/a8.png',
      'assets/images/footer/a9.png',
      'assets/images/footer/a10.png',
      'assets/images/footer/a11.png',
      'assets/images/footer/a12.png'
    ];
    
    // Carousel elements
    const wrapper = document.querySelector('.carousel-wrapper');
    const prevBtn = document.querySelector('.prev-btn');
    const nextBtn = document.querySelector('.next-btn');
    
    // Create slides
    images.forEach(imgSrc => {
      const slide = document.createElement('div');
      slide.className = 'carousel-slide';
      slide.innerHTML = `<img src="${imgSrc}" alt="Game">`;
      wrapper.appendChild(slide);
    });
    
    // Navigation
    let currentIndex = 0;
    const slides = document.querySelectorAll('.carousel-slide');
    const slidesToShow = 5;
    let autoScrollInterval;
    const scrollDelay = 3000; // 3 seconds
    
    function updateCarousel() {
      const offset = -currentIndex * 20; // 20% per slide
      wrapper.style.transform = `translateX(${offset}%)`;
      
      // Disable buttons when at ends
      prevBtn.disabled = currentIndex === 0;
      nextBtn.disabled = currentIndex >= slides.length - slidesToShow;
    }
    
    function nextSlide() {
      if (currentIndex < slides.length - slidesToShow) {
        currentIndex++;
      } else {
        currentIndex = 0; // Loop back to start
      }
      updateCarousel();
    }
    
    function prevSlide() {
      if (currentIndex > 0) {
        currentIndex--;
      } else {
        currentIndex = slides.length - slidesToShow; // Loop to end
      }
      updateCarousel();
    }
    
    function startAutoScroll() {
      autoScrollInterval = setInterval(nextSlide, scrollDelay);
    }
    
    function stopAutoScroll() {
      clearInterval(autoScrollInterval);
    }
    
    // Event listeners
    nextBtn.addEventListener('click', () => {
      stopAutoScroll();
      nextSlide();
      startAutoScroll();
    });
    
    prevBtn.addEventListener('click', () => {
      stopAutoScroll();
      prevSlide();
      startAutoScroll();
    });
    
    // Pause auto-scroll on hover
    wrapper.addEventListener('mouseenter', stopAutoScroll);
    wrapper.addEventListener('mouseleave', startAutoScroll);
    
    // Initialize
    updateCarousel();
    startAutoScroll();
  });
</script>





